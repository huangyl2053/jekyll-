    private void get合議体Map(KaigoNinteiShinsakaiScheduleRelateEntity entity) {
        if (!合議体FLAG) {
            List<RString> gogitaiList = new ArrayList<>(数字9);
            gogitaiMap.put(entity.getKaisaiYMD(), gogitaiList);
            合議体FLAG = true;
        }
        if (gogitaiMap.containsKey(entity.getKaisaiYMD())) {
            List<RString> list = gogitaiMap.get(entity.getKaisaiYMD());
            list.add(entity.getGogitai());
        } else {
            List<RString> pre_list = gogitaiMap.get(predate.toDateString());
            for (int i = pre_list.size(); i < 数字9; i++) {
                pre_list.add(RString.EMPTY);
            }
            List<RString> list = new ArrayList<>();
            list.add(entity.getGogitai());
            gogitaiMap.put(entity.getKaisaiYMD(), list);

        }
        predate = new RDate(entity.getKaisaiYMD().toString());
    }

