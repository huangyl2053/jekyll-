/**
 *
 * 認定調査データ出力（モバイル）調査員情報CSV出力処理クラスです。
 *
 */
public class NinteiChosainDataOutputProcess extends BatchProcessBase<NinteiChosainDataRelateEntity> {

    private static final RString MYBATIS_SELECT_ID = new RString(
            "jp.co.ndensan.reams.db.dbe.persistence.db.mapper.relate.ninteichosadataoutput.INinteiChosaDataOutputMapper."
            + "getChosain");
    private static final EucEntityId EUC_ENTITY_ID = new EucEntityId("DBE224002");
    private static final RString EUC_WRITER_DELIMITER = new RString(",");
    private static final RString EUC_WRITER_ENCLOSURE = new RString("\"");
    private NinteiChosaDataOutputProcessParamter processParamter;
    private FileSpoolManager manager;
    private RString eucFilePath;
    @BatchWriter
    private CsvWriter<NinteiChosainOutputEucCsvEntity> eucCsvWriter;

    @Override
    protected void initialize() {
        manager = new FileSpoolManager(UzUDE0835SpoolOutputType.EucOther, EUC_ENTITY_ID, UzUDE0831EucAccesslogFileType.Csv);
        eucFilePath = Path.combinePath(manager.getEucOutputDirectry(), new RString("調査員情報.csv"));
    }

    @Override
    protected IBatchReader createReader() {
        return new BatchDbReader(MYBATIS_SELECT_ID, processParamter.to認定調査員取得Parameter());
    }

    @Override
    protected void createWriter() {
        eucCsvWriter = new CsvWriter.InstanceBuilder(eucFilePath).
                setDelimiter(EUC_WRITER_DELIMITER).
                setEnclosure(EUC_WRITER_ENCLOSURE).
                setEncode(Encode.SJIS).
                setNewLine(NewLine.CRLF).
                hasHeader(false).
                build();
    }

    @Override
    protected void process(NinteiChosainDataRelateEntity entity) {
        RString 市町村名;
        try {
            市町村名 = AssociationFinderFactory.createInstance().getAssociation(new LasdecCode(entity.get市町村コード())).get市町村名();
        } catch (IllegalArgumentException e) {
            市町村名 = RString.EMPTY;
        }
        eucCsvWriter.writeLine(new NinteiChosainDataOutputResult().setEucCsvEntity(entity, 市町村名));

    }

    @Override
    protected void afterExecute() {
        eucCsvWriter.close();
        manager.spool(eucFilePath);
    }
}
