package jp.co.ndensan.reams.db.dbe.business.core.shujii;

import static java.util.Objects.requireNonNull;
import jp.co.ndensan.reams.db.dbe.definition.core.IshiJokyo;
import jp.co.ndensan.reams.db.dbx.definition.core.valueobject.domain.IryoKikanCode;
import jp.co.ndensan.reams.db.dbz.definition.core.kaigodoctorcode.KaigoDoctorCode;
import jp.co.ndensan.reams.db.dbz.definition.core.kaigoiryokikancode.KaigoIryoKikanCode;
import jp.co.ndensan.reams.uz.uza.biz.AtenaJusho;
import jp.co.ndensan.reams.uz.uza.biz.LasdecCode;
import jp.co.ndensan.reams.uz.uza.biz.TelNo;
import jp.co.ndensan.reams.uz.uza.biz.YubinNo;
import jp.co.ndensan.reams.uz.uza.lang.RString;
import jp.co.ndensan.reams.uz.uza.message.Messages;

/**
 * 主治医の情報を表すクラスです。
 *
 * @author N8156 宮本 康
 */
public class Shujii implements IShujii {

    private final LasdecCode 市町村コード;
    private final KaigoIryoKikanCode 介護医療機関コード;
    private final KaigoDoctorCode 介護医師コード;
    private final IryoKikanCode 医療機関コード;
    private final RString 医師識別番号;
    private final IshiJokyo 医師の状況;
    private final YubinNo 郵便番号;
    private final AtenaJusho 住所;
    private final TelNo 電話番号;
    private final RString ＦＡＸ番号;

    /**
     * インスタンスを生成します。
     *
     * @param 市町村コード 市町村コード
     * @param 介護医療機関コード 介護医療機関コード
     * @param 介護医師コード 介護医師コード
     * @param 医療機関コード 医療機関コード
     * @param 医師識別番号 医師識別番号
     * @param 医師の状況 医師の状況
     * @param 郵便番号 郵便番号
     * @param 住所 住所
     * @param 電話番号 電話番号
     * @param ＦＡＸ番号 ＦＡＸ番号
     */
    public Shujii(LasdecCode 市町村コード, KaigoIryoKikanCode 介護医療機関コード, KaigoDoctorCode 介護医師コード,
            /*I*/ IryoKikanCode 医療機関コード, RString 医師識別番号, IshiJokyo 医師の状況,
            YubinNo 郵便番号, AtenaJusho 住所, TelNo 電話番号, RString ＦＡＸ番号) {
        this.市町村コード = requireNonNull(市町村コード, Messages.E00001.replace("市町村コード").getMessage());
        this.介護医療機関コード = requireNonNull(介護医療機関コード, Messages.E00001.replace("介護医療機関コード").getMessage());
        this.介護医師コード = requireNonNull(介護医師コード, Messages.E00001.replace("介護医師コード").getMessage());
        this.医療機関コード = requireNonNull(医療機関コード, Messages.E00001.replace("医療機関コード").getMessage());
        this.医師識別番号 = requireNonNull(医師識別番号, Messages.E00001.replace("医師識別番号").getMessage());
        this.医師の状況 = requireNonNull(医師の状況, Messages.E00001.replace("医師の状況").getMessage());
        this.郵便番号 = 郵便番号;
        this.住所 = 住所;
        this.電話番号 = 電話番号;
        this.ＦＡＸ番号 = ＦＡＸ番号;
    }

    @Override
    public LasdecCode get市町村コード() {
        return 市町村コード;
    }

    @Override
    public KaigoIryoKikanCode get介護医療機関コード() {
        return 介護医療機関コード;
    }

    @Override
    public KaigoDoctorCode get介護医師コード() {
        return 介護医師コード;
    }

    @Override
    public IryoKikanCode get医療機関コード() {
        return 医療機関コード;
    }

    @Override
    public RString get医師識別番号() {
        return 医師識別番号;
    }

    @Override
    public IshiJokyo get医師の状況() {
        return 医師の状況;
    }

    @Override
    public YubinNo get郵便番号() {
        return 郵便番号;
    }

    @Override
    public AtenaJusho get住所() {
        return 住所;
    }

    @Override
    public TelNo get電話番号() {
        return 電話番号;
    }

    @Override
    public RString getFAX番号() {
        return ＦＡＸ番号;
    }
}
